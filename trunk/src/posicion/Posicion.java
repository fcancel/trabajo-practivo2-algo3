
package posicion;


public class Posicion {
    
    private int posicionX;
    private int posicionY;
    
    public void setX( int numero ){
        
        this.posicionX = numero;        
    }
    
    public int getX(){
        
        return posicionX;
    }

    void setY( int numero ) {
        
        this.posicionY = numero;
    }

    int getY() {
        
        return posicionY;
    }

   
}
