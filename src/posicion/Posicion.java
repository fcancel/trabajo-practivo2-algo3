
package posicion;

/**
 *
 * @author Cancel
 */
public class Posicion {
    
    private int posicionX;
    private int posicionY;
    
    /**
     *
     * @param numero
     */
    public void setX( int numero ){
        
        this.posicionX = numero;        
    }
    
    /**
     *
     * @return
     */
    public int getX(){
        
        return posicionX;
    }

    /**
     *
     * @param numero
     */
    public void setY( int numero ) {
        
        this.posicionY = numero;
    }

    /**
     *
     * @return
     */
    public int getY() {
        
        return posicionY;
    }

   
}
